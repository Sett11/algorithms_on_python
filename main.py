def find_sum(a):
    n,m=len(a),len(a[0])
    r=[[0]*(m+1) for _ in range(n+1)]

    for i in range(1,n+1):
        for j in range(1,m+1):
            r[i][j]=max(r[i-1][j],r[i][j-1])+a[i-1][j-1]
    
    return r[n][m]


